# # Operator
# + Add two operands or unary plus                                     example	x + y
# - Subtract right operand from the left or unary minus                example	x - y
# * Multiply two operands                                              example	x * y
# / Divide left operand by the right one                               example	x / y
# % Modulus - remainder of the division of left operand by the right   example	x % y
# // Floor division - division that results into whole number adjusted
# to the left in the number line                                       example    x // y
# ** Exponent - left operand raised to the power of right              example	x ** y

